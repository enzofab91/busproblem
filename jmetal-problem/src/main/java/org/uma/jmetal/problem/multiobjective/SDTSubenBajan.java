package org.uma.jmetal.problem.multiobjective;

public class SDTSubenBajan {
	private int suben;
	private int bajan;
	
	public SDTSubenBajan(int suben, int bajan){
		this.suben = suben;
		this.bajan = bajan;
	}
	
	public int getSuben(){
		return this.suben;
	}
	
	public int getBajan(){
		return this.bajan;
	}

}
